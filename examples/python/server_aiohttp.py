import logging
import os.path

from aiohttp import web

from perspective import LocalPerspective
from perspective.handlers.aiohttp import PerspectiveAIOHTTPHandler

from datagen import run_datagen


def make_app():
    psp = LocalPerspective()

    # Generate random data
    run_datagen(psp.client)

    async def websocket_handler(request):
        handler = PerspectiveAIOHTTPHandler(perspective=psp, request=request)
        await handler.run()

    app = web.Application()
    app.router.add_get("/websocket", websocket_handler)
    app.router.add_static(
        "/node_modules/@finos", "../../node_modules/@finos", follow_symlinks=True
    )
    app.router.add_static(
        "/node_modules", "../../node_modules/@finos", follow_symlinks=True
    )
    app.router.add_static("/", os.path.dirname(__file__), show_index=True)
    return app


if __name__ == "__main__":
    app = make_app()
    logging.critical("Listening on http://localhost:8082")
    web.run_app(app, host="0.0.0.0", port=8082)
