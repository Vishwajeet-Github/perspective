use pest_derive::Parser;

#[derive(Parser)]
#[grammar = "exprtk/grammar/exprtk.pest"] // relative to src
pub struct ExprTkParser;

#[cfg(test)]
mod test {
    use super::*;
    use pest::Parser;

    #[test]
    fn test_exprtk_parser() {
        let expr = "1,2";
        let pairs = ExprTkParser::parse(Rule::expr, expr).unwrap_or_else(|e| panic!("{}", e));
        for pair in pairs {
            println!("Rule:    {:?}", pair.as_rule());
            println!("Span:    {:?}", pair.as_span());
            println!("Text:    {}", pair.as_str());
            println!("Pairs:   {:?}", pair.into_inner());
            println!("");
        }
    }
}
